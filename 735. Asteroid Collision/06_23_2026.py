# 2. tried to find out when the collision occurs, and noticed that the collision occurs when one element is moving to the left, and the previous one (or the one on the left side) is moving to the right.
# 3. tried simulating one element at a time just like iterating through the input array; spotted that i need to check if the current asteroid is going to collide with the previous one when the current asteroid is moving to the left. at this moment, i ensured this is a stack problem.
# 4. since the current asteroid could collide with not only the adjacent previous one but also all of the previous ones, i needed to keep checking the previous ones (can be easily implemented with a `while` loop) until there are no more collisions.

