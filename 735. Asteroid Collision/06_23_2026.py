from collections import deque


class Solution:
    def asteroidCollision(self, asteroids: List[int]) -> List[int]:
        n = len(asteroids)
        stack = deque([asteroids[0]])

        for i in range(1, n):
            if asteroids[i] > 0:
                stack.append(asteroids[i])
            else:
                is_same_size = False
                while stack and stack[-1] > 0:
                    if stack[-1] < -asteroids[i]:
                        stack.pop()
                    elif stack[-1] == -asteroids[i]:
                        stack.pop()
                        is_same_size = True
                        break
                    else:
                        break
                    
                # negation on `is_same_size or (stack and stack[-1] > 0)`
                if not is_same_size and (not stack or stack[-1] < 0):
                    stack.append(asteroids[i])
                
        return list(stack)
